// Render Workspace File Tree
function renderFileTree() {
  const container = document.getElementById('file-tree-container');
  if (!container) return;

  let html = `
    <div class="tree-folder"><i data-lucide="folder"></i> <span>guidesoft-project</span></div>
  `;

  Object.keys(builderState.files).forEach(filename => {
    const isActive = filename === builderState.activeFile ? 'active' : '';
    html += `
      <div class="tree-file ${isActive}" onclick="openFileInEditor('${filename}')">
        <i data-lucide="file-code"></i>
        <span>${filename}</span>
      </div>
    `;
  });

  container.innerHTML = html;
  if (window.lucide) lucide.createIcons();
}

// Open File in Code Editor
function openFileInEditor(filename) {
  builderState.activeFile = filename;
  renderFileTree();

  const fnElem = document.getElementById('current-filename');
  if (fnElem) fnElem.innerHTML = `<i data-lucide="file-code"></i> <span>${filename}</span>`;

  const codeElem = document.getElementById('editor-code-content');
  if (codeElem) {
    codeElem.innerText = builderState.files[filename] || '// Empty File';
  }

  if (window.lucide) lucide.createIcons();
}

// Render MCP Server List
function renderMcpServerList() {
  const container = document.getElementById('mcp-server-list');
  if (!container) return;

  let html = '';
  builderState.mcpServers.forEach(srv => {
    html += `
      <div class="mcp-card-item">
        <div class="mcp-card-header">
          <span>${srv.name}</span>
          <span style="color:#10b981; font-size:0.7rem;">● ${srv.status}</span>
        </div>
        <div class="mcp-tool-tags">
          ${srv.tools.map(t => `<span class="mcp-tag">${t}</span>`).join('')}
        </div>
      </div>
    `;
  });

  container.innerHTML = html;
}

// Prompt Recipe Presets
function applyRecipe(recipeType) {
  const promptInput = document.getElementById('prompt-input');
  if (!promptInput) return;

  if (recipeType === 'saas') {
    promptInput.value = "Build a multi-tenant SaaS application with PostgreSQL customer storage, Stripe subscription checkout, and automated Resend welcome emails via MCP.";
  } else if (recipeType === 'ai-chat') {
    promptInput.value = "Create a real-time AI Agent platform backed by PostgreSQL vector embeddings and Model Context Protocol routing.";
  } else if (recipeType === 'whatsapp') {
    promptInput.value = "Assemble an autonomous WhatsApp Business bot with lead capture, AI query resolution, and live CRM sync.";
  }
}

// Voice Dictation Input Toggle
function toggleVoiceInput() {
  builderState.isRecordingVoice = !builderState.isRecordingVoice;
  const btn = document.getElementById('voice-btn');

  if (builderState.isRecordingVoice) {
    btn.style.color = '#f43f5e';
    logTerminal('Listening for voice prompt dictation...', 'info');
    setTimeout(() => {
      applyRecipe('saas');
      toggleVoiceInput();
      logTerminal('Voice prompt transcribed successfully.', 'success');
    }, 2500);
  } else {
    btn.style.color = 'inherit';
  }
}

